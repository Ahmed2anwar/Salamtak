export function routeName(key: any, lang?: any) {
  var links: any = routesKeys;
  try {
    return links[key][lang] || 'en/home';
  } catch (error) {
    return links[key][lang] || 'en/home';
    // return 'en/home'
  }
}

export function searchRouteByKey(searchText: any) {
  try {
    const keys = Object.keys(routesKeys);
    for (let key of keys) {
      const langs = Object.keys(routesKeys[key]);
      for (let lang of langs) {
        if (routesKeys[key][lang].includes(searchText)) {
          return routesKeys[key];
        }
      }
    }
    return null;
  } catch (error) {
    console.error('Error searching route key:', error);
    return null;
  }
}
